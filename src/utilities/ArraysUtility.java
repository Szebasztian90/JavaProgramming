package utilities;

public class ArraysUtility {

    // prints each integer of an integer array in separate lines.
    public static void printEach(int[] array){
        for (int each : array) {
            System.out.println(each);
        }
    }


    // prints each DOUBLE of the double array in separate lines.
    public static void printEach(double[] array){
        for (double each : array) {
            System.out.println(each);
        }
    }


    // prints each CHAR of the char array in separate lines
    public static void printEach(char[] array){
        for (char each : array) {
            System.out.println(each);
        }
    }


    //prints each STRING of the string array in separate lines
    public static void printEach(String[] array){
        for (String each : array) {
            System.out.println(each);
        }
    }



}



