package week01;

public class EscapeSequences {
    public static void main(String[] args) {

        /*
        \t : Tab
        \n : Takes it to the next line
        \\ : Prints \
        \" : Prints "

         */
        System.out.println("Learn\tJava\n\tthe\nHard\tWay\n\n");
        System.out.println("\tLearn JAVA the \n\t\" EASY WAY\"\n With Oscar \\\\");


    }
}
